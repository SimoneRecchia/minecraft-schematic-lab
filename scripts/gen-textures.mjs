#!/usr/bin/env node
// scripts/gen-textures.mjs
//
// Writes packages/shared/src/textureData.ts with TEXTURE_NAMES — the block texture base names
// available in apps/web/public/textures/block (filename without .png). Used to resolve which
// blocks have a real texture in the 3D preview. Re-run after adding/removing textures.

import { readdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..');
const texDir = resolve(repoRoot, 'apps/web/public/textures/block');
const outFile = resolve(repoRoot, 'packages/shared/src/textureData.ts');

const names = readdirSync(texDir)
  .filter((f) => f.endsWith('.png'))
  .map((f) => f.replace(/\.png$/, ''))
  .sort();

const body = `// AUTO-GENERATED by scripts/gen-textures.mjs — do not edit by hand.
// Block texture base names available in apps/web/public/textures/block.

export const TEXTURE_NAMES: string[] = ${JSON.stringify(names, null, 2)};
`;

writeFileSync(outFile, body, 'utf8');
process.stdout.write(`Wrote ${outFile} (${names.length} textures)\n`);
